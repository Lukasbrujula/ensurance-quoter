/**
 * Case-insensitive medication lookup by name or alias.
 * Exact match first, then partial match.
 */
export function findMedication(query: string): MedicationEntry | null {
  const q = query.toLowerCase().trim()
  if (!q) return null

  // Exact match on name or alias
  const exact = MEDICATIONS.find(
    (med) =>
      med.name === q ||
      med.aliases.some((a) => a.toLowerCase() === q),
  )
  if (exact) return exact

  // Partial match (query contained in name or alias)
  return (
    MEDICATIONS.find(
      (med) =>
        med.name.includes(q) ||
        med.aliases.some((a) => a.toLowerCase().includes(q)),
    ) ?? null
  )
}
