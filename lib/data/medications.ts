export interface MedicationCarrierEligibility {
  readonly carrierId: string
  readonly result: "accept" | "decline" | "conditional" | "unknown"
  readonly detail?: string
}

export interface MedicationEntry {
  readonly name: string
  readonly aliases: readonly string[]
  readonly condition: string
  readonly carrierResults: readonly MedicationCarrierEligibility[]
  readonly severity: "low" | "moderate" | "high"
}

/* ── Carrier eligibility helpers ────────────────────────────────────── */

function accept(carrierId: string, detail?: string): MedicationCarrierEligibility {
  return detail ? { carrierId, result: "accept", detail } : { carrierId, result: "accept" }
}

function decline(carrierId: string, detail?: string): MedicationCarrierEligibility {
  return detail ? { carrierId, result: "decline", detail } : { carrierId, result: "decline" }
}

function conditional(carrierId: string, detail: string): MedicationCarrierEligibility {
  return { carrierId, result: "conditional", detail }
}

function unknown(carrierId: string): MedicationCarrierEligibility {
  return { carrierId, result: "unknown" }
}

